package appeng.block;


import net.minecraft.client.renderer.ItemMeshDefinition;
import net.minecraftforge.fml.relauncher.Side;
import net.minecraftforge.fml.relauncher.SideOnly;


/**
 * Implement this in a Block subclass to cause a special ItemMeshDefinition to be
 * registered for the block's item, instead of the default model.
 */
public interface IHasItemMeshDefinition
{

	/**
	 * Return the item mesh definition that should be used to determine the item model of an item stack,
	 * instead of the default model.
	 */
	@SideOnly( Side.CLIENT )
	ItemMeshDefinition getItemMeshDefinition();

}
